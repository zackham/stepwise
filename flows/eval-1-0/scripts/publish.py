#!/usr/bin/env python3
"""Publish evaluation report as self-contained HTML for eval-1.0.

Reads report content (markdown) and human decision, generates HTML report.
If human rejected, outputs status without generating HTML.
"""

import json
import os
import sys
from datetime import datetime, timezone

HTML_TEMPLATE = """<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>Stepwise 1.0 Evaluation — Run {run_number}</title>
<style>
  * {{ margin: 0; padding: 0; box-sizing: border-box; }}
  body {{ font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif;
         background: #0f172a; color: #e2e8f0; line-height: 1.6; padding: 2rem; }}
  .container {{ max-width: 900px; margin: 0 auto; }}
  h1 {{ color: #f8fafc; margin-bottom: 0.5rem; font-size: 1.75rem; }}
  h2 {{ color: #94a3b8; margin: 2rem 0 1rem; font-size: 1.25rem;
        border-bottom: 1px solid #334155; padding-bottom: 0.5rem; }}
  h3 {{ color: #cbd5e1; margin: 1.5rem 0 0.5rem; font-size: 1.1rem; }}
  .meta {{ color: #64748b; margin-bottom: 2rem; font-size: 0.875rem; }}
  .badge {{ display: inline-block; padding: 0.25rem 0.75rem; border-radius: 9999px;
            font-size: 0.75rem; font-weight: 600; text-transform: uppercase; }}
  .badge-go {{ background: #065f46; color: #6ee7b7; }}
  .badge-nogo {{ background: #7f1d1d; color: #fca5a5; }}
  .badge-rejected {{ background: #78350f; color: #fcd34d; }}
  .badge-overridden {{ background: #312e81; color: #a5b4fc; }}
  .score-box {{ display: inline-flex; align-items: center; gap: 1rem;
                background: #1e293b; border: 1px solid #334155; border-radius: 0.5rem;
                padding: 1rem 1.5rem; margin: 1rem 0; }}
  .score-number {{ font-size: 2.5rem; font-weight: 700; }}
  .score-label {{ color: #94a3b8; font-size: 0.875rem; }}
  .score-green {{ color: #6ee7b7; }}
  .score-yellow {{ color: #fcd34d; }}
  .score-red {{ color: #fca5a5; }}
  table {{ width: 100%; border-collapse: collapse; margin: 1rem 0; }}
  th, td {{ padding: 0.5rem 0.75rem; text-align: left; border-bottom: 1px solid #1e293b; }}
  th {{ color: #94a3b8; font-size: 0.75rem; text-transform: uppercase; letter-spacing: 0.05em;
        background: #1e293b; }}
  td {{ font-size: 0.875rem; }}
  .pass {{ color: #6ee7b7; }}
  .fail {{ color: #fca5a5; }}
  .warn {{ color: #fcd34d; }}
  .gate-pass {{ color: #6ee7b7; font-weight: 600; }}
  .gate-fail {{ color: #fca5a5; font-weight: 600; }}
  pre {{ background: #1e293b; border: 1px solid #334155; border-radius: 0.5rem;
         padding: 1rem; overflow-x: auto; font-size: 0.875rem; white-space: pre-wrap; }}
  .report-content {{ background: #1e293b; border: 1px solid #334155; border-radius: 0.5rem;
                     padding: 1.5rem; margin: 1rem 0; white-space: pre-wrap;
                     font-size: 0.875rem; line-height: 1.8; }}
  .human-notes {{ background: #1e293b; border-left: 3px solid #6366f1;
                  padding: 1rem; margin: 1rem 0; font-style: italic; }}
  .footer {{ margin-top: 3rem; padding-top: 1rem; border-top: 1px solid #334155;
             color: #475569; font-size: 0.75rem; text-align: center; }}
</style>
</head>
<body>
<div class="container">
  <h1>Stepwise 1.0 Readiness Evaluation</h1>
  <div class="meta">
    Run #{run_number} &middot; {date} &middot;
    <span class="{recommendation_class}">{recommendation}</span>
    {human_badge}
  </div>

  <div class="score-box">
    <div>
      <div class="score-number {score_color}">{overall_avg}%</div>
      <div class="score-label">Overall Score</div>
    </div>
  </div>

  <h2>Hard Gates</h2>
  <table>
    <thead>
      <tr><th>Gate</th><th>Score</th><th>Threshold</th><th>Status</th></tr>
    </thead>
    <tbody>
      {gate_rows}
    </tbody>
  </table>

  <h2>Scorecard</h2>
  <table>
    <thead>
      <tr><th>Dimension</th><th>Pass</th><th>Fail</th><th>Insuff.</th><th>Score</th></tr>
    </thead>
    <tbody>
      {scorecard_rows}
    </tbody>
  </table>

  <h2>Full Report</h2>
  <div class="report-content">{report_content}</div>

  {human_section}

  <div class="footer">
    Generated by Stepwise eval-1.0 &middot; {date}
  </div>
</div>
</body>
</html>"""


def parse_json_env(name, default=None):
    val = os.environ.get(name, "")
    if not val:
        return default
    try:
        return json.loads(val)
    except (json.JSONDecodeError, TypeError):
        return val if default is None else default


def escape_html(s):
    """Escape HTML special characters."""
    if not isinstance(s, str):
        s = str(s)
    return s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;").replace('"', "&quot;")


def main():
    report_content = os.environ.get("report_content", "No report content available.")
    human_decision = os.environ.get("human_decision", "approve")
    human_notes = os.environ.get("human_notes", "")
    recommendation = os.environ.get("recommendation", "NO-GO")
    scorecard = parse_json_env("scorecard", {})
    overall_avg = os.environ.get("overall_avg", "0")
    gates = parse_json_env("gates", {})
    security_veto = os.environ.get("security_veto", "false").lower() in ("true", "1")
    run_number = os.environ.get("run_number", "1")
    stepwise_path = os.environ.get("stepwise_path", ".")

    # If human rejected, don't generate report
    if human_decision == "reject":
        output = {
            "report_path": "",
            "status": "rejected",
            "human_decision": human_decision,
            "run_number": run_number,
        }
        print(json.dumps(output))
        return

    # If human overrode, flip recommendation
    final_recommendation = recommendation
    if human_decision == "override":
        final_recommendation = "GO" if recommendation == "NO-GO" else "NO-GO"

    # Format date
    now = datetime.now(timezone.utc)
    date_str = now.strftime("%Y-%m-%d %H:%M UTC")
    date_file = now.strftime("%Y%m%d-%H%M")

    # Score color
    try:
        avg_num = float(overall_avg)
    except (ValueError, TypeError):
        avg_num = 0
    if avg_num >= 80:
        score_color = "score-green"
    elif avg_num >= 60:
        score_color = "score-yellow"
    else:
        score_color = "score-red"

    # Recommendation class
    rec_class = "badge badge-go" if final_recommendation == "GO" else "badge badge-nogo"

    # Human badge
    human_badge = ""
    if human_decision == "override":
        human_badge = ' <span class="badge badge-overridden">OVERRIDDEN</span>'
    elif human_decision == "reject":
        human_badge = ' <span class="badge badge-rejected">REJECTED</span>'

    # Gate rows
    gate_rows = ""
    for name, g in (gates.items() if isinstance(gates, dict) else []):
        status_class = "gate-pass" if g.get("passed") else "gate-fail"
        status_text = "PASS" if g.get("passed") else "FAIL"
        if name == "security" and security_veto:
            status_text = "FAIL (BLOCKER VETO)"
        gate_rows += (
            f'<tr><td>{escape_html(name)}</td>'
            f'<td>{g.get("score_pct", 0)}%</td>'
            f'<td>≥{g.get("threshold", 80)}%</td>'
            f'<td class="{status_class}">{status_text}</td></tr>\n'
        )

    # Scorecard rows
    scorecard_rows = ""
    for dim_name, dim in (scorecard.items() if isinstance(scorecard, dict) else []):
        score = dim.get("score_pct", 0)
        score_class = "pass" if score >= 80 else ("warn" if score >= 60 else "fail")
        scorecard_rows += (
            f'<tr><td>{escape_html(dim_name)}</td>'
            f'<td class="pass">{dim.get("pass_count", 0)}</td>'
            f'<td class="fail">{dim.get("fail_count", 0)}</td>'
            f'<td class="warn">{dim.get("insufficient_count", 0)}</td>'
            f'<td class="{score_class}">{score}%</td></tr>\n'
        )

    # Human section
    human_section = ""
    if human_notes:
        human_section = f'<h2>Human Review Notes</h2>\n<div class="human-notes">{escape_html(human_notes)}</div>'

    # Generate HTML
    html = HTML_TEMPLATE.format(
        run_number=escape_html(str(run_number)),
        date=date_str,
        recommendation=escape_html(final_recommendation),
        recommendation_class=rec_class,
        human_badge=human_badge,
        overall_avg=escape_html(str(overall_avg)),
        score_color=score_color,
        gate_rows=gate_rows,
        scorecard_rows=scorecard_rows,
        report_content=escape_html(report_content),
        human_section=human_section,
    )

    # Write report
    reports_dir = os.path.join(stepwise_path, "reports")
    os.makedirs(reports_dir, exist_ok=True)
    filename = f"eval-1.0-run-{run_number}-{date_file}.html"
    report_path = os.path.join(reports_dir, filename)

    with open(report_path, "w") as f:
        f.write(html)

    output = {
        "report_path": report_path,
        "status": "published",
        "human_decision": human_decision,
        "run_number": run_number,
    }
    print(json.dumps(output))


if __name__ == "__main__":
    main()
